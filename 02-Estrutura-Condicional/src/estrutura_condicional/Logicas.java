package estrutura_condicional;

public class Logicas {
	
	public static void main(String[] args) {
		
		
		// Operador E-&& todas verdadeiras = true
		
		int x;
		
		x = 5;
		
		System.out.println(x <= 20 && x == 10);
		System.out.println(x <= 20 && x == 5);
		
		// operador OU-||
		
		System.out.println(x <= 20 || x == 10);
		System.out.println(x < 3 || x != 5);
		
		// não ! inverte a condição
		
		

	}

}